    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String product=txtSaleProduct.getText();
        String unit=txtSaleUnit.getText();
        String price=txtSalePrice.getText();
        String quantity=txtSaleQuantity.getText();
        if(quantity.length()==0){
            JOptionPane.showMessageDialog(null, "Enter quantity");
        }else{
            String subtotal=Integer.toString(Integer.parseInt(quantity)*Integer.parseInt(price));
            DefaultTableModel dtm = (DefaultTableModel)tblSales.getModel();
            Object[] row = {product,unit,price,quantity,subtotal};
            dtm.addRow(row);
            tblSales.setModel(dtm);
            int total=Integer.parseInt(labelTotal.getText());
            int newTotal=total+Integer.parseInt(subtotal);
            labelTotal.setText(Integer.toString(newTotal));
        }
    }//GEN-LAST:event_jButton4ActionPerformed


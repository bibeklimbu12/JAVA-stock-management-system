    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        try{
        int row=tblSaleProducts.getSelectedRow();
        if (row<0){
           JOptionPane.showMessageDialog(null, "Select a row to update");
       }else{
            String productID=tblSaleProducts.getValueAt(row, 0).toString();
            String productName=txtUpdateProduct.getText();
            String unit =txtUpdateUnit.getText();
            String quantity=txtUpdateQuantity.getText();
            String price=txtUpdatePrice.getText();
            String inventory=txtUpdateInventory.getText();
            if(Conn.Update("UPDATE product SET name='"+productName+"',um='"+unit+"',quantityInStock='"+quantity+"',minInventory='"+inventory+"',price='"+price+"' WHERE productID='"+productID+"'")==true){
                JOptionPane.showMessageDialog(null, "Record updated successfully");
                tblSaleProducts.setModel(Conn.PopulateTable("select * from product"));
                txtUpdateProduct.setText("");
                txtUpdateUnit.setText("");
                txtUpdateQuantity.setText("");
                txtUpdatePrice.setText("");
                txtUpdateInventory.setText("");
            }else{
                JOptionPane.showMessageDialog(null, "Error updating product");
            }
        }
        }catch (Exception e){
            
        }
    }//GEN-LAST:event_jButton7ActionPerformed


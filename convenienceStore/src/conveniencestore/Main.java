    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String productName=txtName.getText();
        String units=txtUm.getText();
        String quantity=txtQuantity.getText();
        String price=txtPrice.getText();
        String min=txtPrice.getText();
        if (productName.length()==0 || units.length()==0 || quantity.length()==0 || price.length()==0 || min.length()==0){
            JOptionPane.showMessageDialog(null,"All fields are supposed to be filled");
        }else{
            String sql="INSERT INTO product (name,quantityInStock,minInventory,price,um) VALUES ('"+productName+"','"+quantity+"','"+min+"','"+price+"','"+units+"')";
            if(Conn.Insert(sql)==true){
                tblSaleProducts.setModel(Conn.PopulateTable("Select * from product"));
                JOptionPane.showMessageDialog(this, "Product saved successfully");
                txtName.setText("");
                txtUm.setText("");
                txtQuantity.setText("");
                txtPrice.setText("");
                txtPrice.setText("");
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

